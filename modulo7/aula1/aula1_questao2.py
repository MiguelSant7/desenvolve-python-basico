## Programa para unir duas strings(Sobrenome)

# Entrada de nome e sobrenome
nome      = input("Informe seu nome, por favor: ")
sobrenome = input("Informe seu sobrenome, por favor: ")

# Mensagem de boas vindas
mensagem_boas_vindas = "Bem Vindo(a)"

# Concatenação de strings
print(f"{mensagem_boas_vindas}, {nome} {sobrenome}")